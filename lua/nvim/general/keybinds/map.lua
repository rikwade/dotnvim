local Shortcut = require('nvim.utils.nvim.shortcut')

Shortcut():mode(''):options():noremap():next():keymaps({
    ----------------------------------------------------------------------
    --                          vim arrow keys                          --
    ----------------------------------------------------------------------
    -- left
    { 'm', 'h' },
    { 'M', 'H' },

    { 'h', 'i' },
    { 'H', 'I' },

    -- down
    { 'n', 'j' },
    { 'N', 'J' },

    { 'k', 'n' },
    { 'K', 'N' },

    -- up
    { 'e', 'k' },
    { 'E', 'K' },

    { 'l', 'e' },
    { 'L', 'E' },

    -- right
    { 'i', 'l' },
    { 'I', 'L' },

    { 'j', ';' },
    { 'J', ':' },

    { ';', 'm' },
    { ':', 'M' },

})
